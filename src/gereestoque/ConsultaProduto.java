    private void Con_ForMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Con_ForMouseClicked
        p.nomeP = this.cad_NomeP.getText();
        p.preco=Float.parseFloat(this.cad_precoP.getText());
        p.qnt =Integer.parseInt(this.cad_qntP.getText());
        p.qntM = Integer.parseInt(this.cad_qdtMinP.getText());
        p.validade = this.cad_validadeP.getText();
        p.codFor = Integer.parseInt(this.cad_codForP.getText());
        p.codP= Integer.parseInt(this.busca_pro.getText());
        Conexao connection = Conexao.getInstance();
            String sql = "UPDATE produto SET nome =? ,qtd =?,qdtM = ?,preco = ?, validade = ?, cod_for = ? WHERE id = ? ";
        try {
            PreparedStatement stm = connection.getConnection().prepareStatement(sql);
            stm.setString(1,p.nomeP);
            stm.setInt(2,p.qnt);
            stm.setInt(3,p.qntM);
            stm.setDouble(4,p.preco);
            stm.setString(5,p.validade);
            stm.setInt(6,p.codFor);
            stm.setInt(7,p.codP);
            
            stm.executeUpdate();
            JOptionPane.showMessageDialog(null, "Alteração realizada");

        }catch(SQLException e){
            System.out.println("ERRO:"+ e);
        }
        finally {
            connection.destroy();
        }

    }//GEN-LAST:event_Con_ForMouseClicked

