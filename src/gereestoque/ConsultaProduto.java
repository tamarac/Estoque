    private void pesq_proMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pesq_proMouseClicked
        // TODO add your handling code here:
  
        Integer num = Integer.parseInt(busca_pro.getText());
        ProdutoDAO dao = new ProdutoDAO();
        this.p =  dao.buscaProduto(num);
        cad_NomeP.setText(p.nomeP);
        cad_precoP.setText(Integer.toString((int) p.preco));
        cad_qntP.setText(Integer.toString(p.qnt));
        cad_qdtMinP.setText(Integer.toString(p.qntM));
        cad_validadeP.setText(p.validade);
       if (p.qnt <= p.qntM){
           Qnt_veri.setText("Necessário Comprar Produto!");
       }
       
    }//GEN-LAST:event_pesq_proMouseClicked

